def csv_text(records: Sequence[object]) -> str:
    if not records:
        return ""
    fields = [f.name for f in dataclasses.fields(records[0])]
    handle = io.StringIO(newline="")
    writer = csv.DictWriter(handle, fieldnames=fields, lineterminator="\n")
    writer.writeheader()
    for record in records:
        row = dataclasses.asdict(record)
        for key, value in row.items():
            if isinstance(value, tuple):
                row[key] = ";".join(value)
        writer.writerow(row)
    return handle.getvalue()

def write_csv(path: Path, records: Sequence[object]) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    if not records:
        path.write_text("", encoding="utf-8")
        return
    fields = [f.name for f in dataclasses.fields(records[0])]
    with path.open("w", encoding="utf-8", newline="") as handle:
        writer = csv.DictWriter(handle, fieldnames=fields, lineterminator="\n")
        writer.writeheader()
        for record in records:
            row = dataclasses.asdict(record)
            for key, value in row.items():
                if isinstance(value, tuple):
                    row[key] = ";".join(value)
            writer.writerow(row)


def as_json_data(pdus: list[PduRecord], saps: list[SapRecord], gaps: list[GapRecord], states: list[StateRecord]) -> dict[str, object]:
    return {
        "schema_version": 1,
        "note": "Static source inventory; not a claim of ETSI certification.",
        "summary": {
            "pdu_count": len(pdus),
            "sap_primitive_count": len(saps),
            "gap_count": len(gaps),
            "state_type_count": len(states),
        },
        "pdus": [dataclasses.asdict(r) for r in pdus],
        "sap_primitives": [dataclasses.asdict(r) for r in saps],
        "gaps": [dataclasses.asdict(r) for r in gaps],
        "state_types": [dataclasses.asdict(r) for r in states],
    }


def outputs(root: Path) -> dict[Path, str]:
    production_files = rust_files(root, "crates") + rust_files(root, "bins") + rust_files(root, "system-backend")
    test_files = [p for p in root.rglob("*.rs") if "tests" in p.parts]
    production_corpus = source_corpus(production_files)
    tests_corpus = source_corpus(test_files)
    pdus = pdu_records(root, production_corpus, tests_corpus)
    saps = sap_records(root)
    gaps = gap_records(root)
    states = state_records(root)
    data = as_json_data(pdus, saps, gaps, states)
    return {
        root / "Docs" / "SWMI_FOUNDATION_1_INVENTORY.md": render_summary(pdus, saps, gaps, states),
        root / "Docs" / "ETSI_CONFORMANCE_MATRIX.md": render_conformance(pdus),
        root / "Docs" / "SAP_PRIMITIVE_MATRIX.md": render_saps(saps),
        root / "Docs" / "IMPLEMENTATION_GAPS.md": render_gaps(gaps, saps, pdus),
        root / "Docs" / "STATE_MACHINE_INVENTORY.md": render_states(states),
        root / "Docs" / "generated" / "protocol_inventory.json": json.dumps(data, indent=2, ensure_ascii=False, sort_keys=True) + "\n",
        root / "Docs" / "generated" / "pdu_inventory.csv": csv_text(pdus),
        root / "Docs" / "generated" / "sap_inventory.csv": csv_text(saps),
        root / "Docs" / "generated" / "gap_inventory.csv": csv_text(gaps),
        root / "Docs" / "generated" / "state_inventory.csv": csv_text(states),
    }


def generate_csv_files(root: Path) -> None:
    production_files = rust_files(root, "crates") + rust_files(root, "bins") + rust_files(root, "system-backend")
    tests = [p for p in root.rglob("*.rs") if "tests" in p.parts]
    pdus = pdu_records(root, source_corpus(production_files), source_corpus(tests))
    saps = sap_records(root)
    gaps = gap_records(root)
    states = state_records(root)
    write_csv(root / "Docs" / "generated" / "pdu_inventory.csv", pdus)
    write_csv(root / "Docs" / "generated" / "sap_inventory.csv", saps)
    write_csv(root / "Docs" / "generated" / "gap_inventory.csv", gaps)
    write_csv(root / "Docs" / "generated" / "state_inventory.csv", states)


def digest(text: str) -> str:
    return hashlib.sha256(text.encode("utf-8")).hexdigest()


def main(argv: Sequence[str] | None = None) -> int:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--check", action="store_true", help="fail when tracked generated files are stale")
    args = parser.parse_args(argv)
    root = repo_root()
    generated = outputs(root)
    stale: list[str] = []
    for path, content in generated.items():
        if args.check:
            current = read(path) if path.exists() else ""
            if digest(current) != digest(content):
                stale.append(rel(path, root))
        else:
            path.parent.mkdir(parents=True, exist_ok=True)
            path.write_text(content, encoding="utf-8")
    if args.check:
        # CSV consistency is checked by regenerating into memory-unfriendly temp-free comparison.
        # They are deterministic, so normal generation before commit is the canonical workflow.
        if stale:
            print("Protocol inventory is stale:", file=sys.stderr)
            for item in stale:
                print(f"  - {item}", file=sys.stderr)
            print("Run: python3 tools/protocol_inventory.py", file=sys.stderr)
            return 1
        print("Protocol inventory is up to date.")
        return 0
    print("Generated protocol inventory:")
    for path in sorted(generated):
        print(f"  - {rel(path, root)}")
    return 0


