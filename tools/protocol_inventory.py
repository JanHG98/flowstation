def repo_root() -> Path:
    return Path(__file__).resolve().parents[1]


def rel(path: Path, root: Path) -> str:
    return path.relative_to(root).as_posix()


def read(path: Path) -> str:
    return path.read_text(encoding="utf-8", errors="replace")


def rust_files(root: Path, *parts: str) -> list[Path]:
    base = root.joinpath(*parts)
    return sorted(p for p in base.rglob("*.rs") if "target" not in p.parts)


def extract_function(text: str, name: str) -> str | None:
    """Return a Rust function including its body using lightweight brace matching."""
    match = re.search(rf"\bfn\s+{re.escape(name)}\s*[^{{;]*\{{", text, re.S)
    if not match:
        return None
    start = match.start()
    brace = text.find("{", match.start(), match.end())
    if brace < 0:
        return None
    depth = 0
    in_string = False
    escaped = False
    for idx in range(brace, len(text)):
        ch = text[idx]
        if in_string:
            if escaped:
                escaped = False
            elif ch == "\\":
                escaped = True
            elif ch == '"':
                in_string = False
            continue
        if ch == '"':
            in_string = True
        elif ch == "{":
            depth += 1
        elif ch == "}":
            depth -= 1
            if depth == 0:
                return text[start : idx + 1]
    return text[start:]


def blockers_in(text: str | None) -> tuple[str, ...]:
    if text is None:
        return ()
    patterns = {
        "unimplemented!": r"\bunimplemented!\s*\(",
        "todo!": r"\btodo!\s*\(",
        "panic!": r"\bpanic!\s*\(",
        "unreachable!": r"\bunreachable!\s*\(",
        "TODO": r"\bTODO\b",
        "FIXME": r"\bFIXME\b",
    }
    return tuple(name for name, pat in patterns.items() if re.search(pat, text))


def codec_status(function: str | None) -> str:
    if function is None:
        return "fehlt"
    blockers = blockers_in(function)
    if any(b in blockers for b in ("unimplemented!", "todo!", "panic!", "unreachable!")):
        return "teilweise"
    if "TODO" in blockers or "FIXME" in blockers:
        return "teilweise"
    return "vorhanden"


def direction_for(filename: str) -> str:
    stem = Path(filename).stem
    if stem.startswith("d_"):
        return "Downlink"
    if stem.startswith("u_"):
        return "Uplink"
    if stem.startswith("bl_"):
        return "bidirektional"
    if stem.startswith("mac_") or stem.startswith("access_"):
        return "MAC/abhängig"
    return "nicht klassifiziert"


def phase_for(layer: str, name: str, path: str) -> str:
    lower = f"{name} {path}".lower()
    if "restore" in lower and layer == "CMCE":
        return "Phase 5 – CMCE Restore"
    if layer == "MLE":
        return "Phase 3 – MLE/Zellwechsel"
    if layer == "MM":
        return "Phase 4 – Mobility Management"
    if layer == "CMCE":
        return "Phase 5/13 – Call Control/SS"
    if layer in {"LLC", "UMAC"}:
        return "Phase 0 – Protokollfundament"
    return "späteres Profil"


def relevance_for(layer: str, name: str) -> str:
    if layer in {"UMAC", "LLC"}:
        return "Single-Site, Funkzugang"
    if layer == "MLE":
        return "Multi-Site, Zellwechsel"
    if layer == "MM":
        return "Single-/Multi-Site, Mobility"
    if layer == "CMCE":
        if "Sds" in name or "Status" in name:
            return "SDS/Status"
        return "Calls, Single-/Multi-Site"
    return "allgemein"


def source_corpus(files: Sequence[Path]) -> dict[Path, str]:
    return {p: read(p) for p in files}


def count_word(corpus: dict[Path, str], word: str, excluded: set[Path] | None = None) -> int:
    excluded = excluded or set()
    pat = re.compile(rf"\b{re.escape(word)}\b")
    return sum(len(pat.findall(text)) for path, text in corpus.items() if path not in excluded)


def pdu_records(root: Path, all_corpus: dict[Path, str], test_corpus: dict[Path, str]) -> list[PduRecord]:
    records: list[PduRecord] = []
    pdu_root = root / "crates" / "tetra-pdus" / "src"
    for path in sorted(pdu_root.glob("*/pdus/*.rs")):
        if path.name == "mod.rs":
            continue
        text = read(path)
        structs = re.findall(r"\bpub\s+struct\s+([A-Za-z0-9_]+)", text)
        if not structs:
            continue
        name = structs[0]
        layer = path.relative_to(pdu_root).parts[0].upper()
        parser_fn = extract_function(text, "from_bitbuf")
        encoder_fn = extract_function(text, "to_bitbuf")
        parser = codec_status(parser_fn)
        encoder = codec_status(encoder_fn)
        file_blockers = tuple(sorted(set(blockers_in(text))))
        runtime = "blockiert" if any(x in file_blockers for x in ("unimplemented!", "todo!", "panic!", "unreachable!")) else (
            "offene Hinweise" if any(x in file_blockers for x in ("TODO", "FIXME")) else "ohne offensichtlichen Blocker"
        )
        runtime_refs = count_word(all_corpus, name, {path})
        test_refs = count_word(test_corpus, name)
        tests = "vorhanden" if test_refs else "fehlt"
        golden = "vorhanden" if any(
            name in text and re.search(r"bitstr|golden|expected|hex|roundtrip", text, re.I)
            for text in test_corpus.values()
        ) else "nicht nachgewiesen"
        path_rel = rel(path, root)
        records.append(
            PduRecord(
                layer=layer,
                name=name,
                direction=direction_for(path.name),
                path=path_rel,
                parser=parser,
                encoder=encoder,
                runtime=runtime,
                tests=tests,
                golden_vector=golden,
                runtime_references=runtime_refs,
                test_references=test_refs,
                blockers=file_blockers,
                relevance=relevance_for(layer, name),
                phase=phase_for(layer, name, path_rel),
            )
        )
    return records


def parse_sapmsg_variants(text: str) -> set[str]:
    match = re.search(r"pub\s+enum\s+SapMsgInner\s*\{(.*?)\n\}", text, re.S)
    if not match:
        return set()
    body = match.group(1)
    variants: set[str] = set()
    for line in body.splitlines():
        stripped = line.strip()
        if not stripped or stripped.startswith("//") or stripped.startswith("///"):
            continue
        m = re.match(r"([A-Z][A-Za-z0-9_]*)\s*(?:\(|\{|,)", stripped)
        if m:
            variants.add(m.group(1))
    return variants


def count_variant_usage(files: Sequence[Path], variant: str) -> tuple[int, int]:
    constructed = 0
    handled = 0
    pat = re.compile(rf"SapMsgInner::{re.escape(variant)}\b")
    for path in files:
        text = read(path)
        for line in text.splitlines():
            if not pat.search(line):
                continue
            if re.search(rf"msg\s*:\s*SapMsgInner::{re.escape(variant)}\b", line):
                constructed += 1
            else:
                handled += 1
    return constructed, handled


def sap_records(root: Path) -> list[SapRecord]:
    sap_root = root / "crates" / "tetra-saps" / "src"
    sap_files = sorted(
        p for p in sap_root.rglob("*.rs")
        if p.name not in {"lib.rs", "sapmsg.rs"}
        and "enums" not in p.parts
        and "fields" not in p.parts
    )
    entity_files = rust_files(root, "crates", "tetra-entities", "src") + rust_files(root, "bins")
    test_files = [p for p in root.rglob("*.rs") if "tests" in p.parts]
    variants = parse_sapmsg_variants(read(sap_root / "sapmsg.rs"))
    records: list[SapRecord] = []
    for path in sap_files:
        text = read(path)
        names = re.findall(r"\bpub\s+(?:struct|enum)\s+([A-Z][A-Za-z0-9_]*)", text)
        for name in names:
            if not re.search(r"(?:Req|Ind|Conf|Confirm|Resp|Demand|Response|Update|Data)$", name):
                continue
            constructed, handled = count_variant_usage(entity_files, name)
            tests = sum(read(p).count(name) for p in test_files)
            if name not in variants:
                status = "nicht in SapMsgInner verdrahtet"
                variant = "nein"
            elif constructed == 0 and handled == 0:
                status = "Variante vorhanden, Runtime-Pfad fehlt"
                variant = "ja"
            elif constructed == 0:
                status = "nur Empfang/Handler sichtbar"
                variant = "ja"
            elif handled == 0:
                status = "nur Erzeugung sichtbar"
                variant = "ja"
            else:
                status = "bidirektional sichtbar"
                variant = "ja"
            records.append(
                SapRecord(
                    sap=path.relative_to(sap_root).parts[0].upper(),
                    primitive=name,
                    path=rel(path, root),
                    sapmsg_variant=variant,
                    constructed=constructed,
                    handled=handled,
                    tests=tests,
                    status=status,
                )
            )
    return sorted(records, key=lambda r: (r.sap, r.primitive))


GAP_PATTERNS = (
    ("unimplemented!", "kritisch", re.compile(r"\bunimplemented!\s*\(")),
    ("todo!", "kritisch", re.compile(r"\btodo!\s*\(")),
    ("panic!", "prüfen", re.compile(r"\bpanic!\s*\(")),
    ("unreachable!", "prüfen", re.compile(r"\bunreachable!\s*\(")),
    ("unimplemented_log!", "offener Runtime-Pfad", re.compile(r"\bunimplemented_log!\s*\(")),
    ("Todo-Typ", "Typ-Platzhalter", re.compile(r"\bTodo\b")),
    ("TODO/FIXME", "technische Schuld", re.compile(r"\b(?:TODO|FIXME)\b")),
)


def gap_records(root: Path) -> list[GapRecord]:
    records: list[GapRecord] = []
    for path in rust_files(root, "crates") + rust_files(root, "bins") + rust_files(root, "system-backend"):
        scope = "Test" if "tests" in path.parts or path.name.endswith("_test.rs") else "Runtime"
        for line_no, line in enumerate(read(path).splitlines(), start=1):
            stripped = line.strip()
            # Runtime macros in comments are historical notes, not executable paths.
            # TODO/FIXME and explicit Todo type placeholders remain part of the inventory.
            if stripped.startswith("//") or stripped.startswith("///"):
                active_patterns = tuple(item for item in GAP_PATTERNS if item[0] in {"TODO/FIXME", "Todo-Typ"})
            else:
                active_patterns = GAP_PATTERNS
            for category, severity, pattern in active_patterns:
                if pattern.search(line):
                    effective_severity = severity
                    if scope == "Test" and category in {"panic!", "unreachable!"}:
                        effective_severity = "Test-Assertion"
                    records.append(
                        GapRecord(
                            category=category,
                            severity=effective_severity,
                            scope=scope,
                            path=rel(path, root),
                            line=line_no,
                            text=stripped[:240],
                        )
                    )
    return records


def enum_body(text: str, name: str) -> str | None:
    match = re.search(rf"\benum\s+{re.escape(name)}\s*\{{", text)
    if not match:
        return None
    brace = text.find("{", match.start(), match.end())
    depth = 0
    for idx in range(brace, len(text)):
        if text[idx] == "{":
            depth += 1
        elif text[idx] == "}":
            depth -= 1
            if depth == 0:
                return text[brace + 1 : idx]
    return None


def state_records(root: Path) -> list[StateRecord]:
    entity_root = root / "crates" / "tetra-entities" / "src"
    sap_root = root / "crates" / "tetra-saps" / "src"
    files = rust_files(root, "crates", "tetra-entities", "src") + rust_files(root, "crates", "tetra-saps", "src")
    tests = {p: read(p) for p in root.rglob("*.rs") if "tests" in p.parts}
    result: list[StateRecord] = []
    for path in files:
        text = read(path)
        for name in re.findall(r"\b(?:pub\s+)?enum\s+([A-Za-z0-9_]*State[A-Za-z0-9_]*)\s*\{", text):
            body = enum_body(text, name) or ""
            variants = 0
            for line in body.splitlines():
                stripped = re.sub(r"//.*$", "", line).strip()
                if re.match(r"^[A-Z][A-Za-z0-9_]*(?:\s*\([^)]*\)|\s*\{[^}]*\})?\s*,?", stripped):
                    variants += 1
            if path.is_relative_to(entity_root):
                parts = path.relative_to(entity_root).parts
                layer = parts[0].upper() if parts else "UNKNOWN"
            else:
                parts = path.relative_to(sap_root).parts
                layer = "SAP/" + (parts[0].upper() if parts else "COMMON")
            test_refs = sum(t.count(name) for t in tests.values())
            result.append(StateRecord(layer, name, rel(path, root), variants, test_refs))
    return sorted(result, key=lambda r: (r.layer, r.name, r.path))


def md(value: object) -> str:
    return str(value).replace("|", "\\|").replace("\n", " ")


def table(headers: Sequence[str], rows: Iterable[Sequence[object]]) -> str:
    lines = ["| " + " | ".join(headers) + " |", "| " + " | ".join("---" for _ in headers) + " |"]
    lines.extend("| " + " | ".join(md(v) for v in row) + " |" for row in rows)
    return "\n".join(lines) + "\n"


